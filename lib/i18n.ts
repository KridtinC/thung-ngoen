const dict: Record<Lang, Dict> = { en, th };

export function t(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  const table = dict[lang] || dict.en;
  let s = table[key] ?? dict.en[key] ?? key;
  if (vars) for (const [k, v] of Object.entries(vars)) s = s.split(`{${k}}`).join(String(v));
  return s;
}

// Detect the UI language: saved choice → system hint → default 'en'.
export function detectLang(systemLangHint?: string): Lang {
  try {
    const saved = typeof localStorage !== 'undefined' ? localStorage.getItem('lang') : null;
    if (saved === 'en' || saved === 'th') return saved;
  } catch { /* ignore */ }
  const sys = systemLangHint || (typeof navigator !== 'undefined' ? navigator.language : '') || 'en';
  return String(sys).toLowerCase().startsWith('th') ? 'th' : 'en';
}

// For tests / sanity: keys present in one language but missing in the other.
export function keyParity(): { inEnOnly: string[]; inThOnly: string[] } {
  const eK = Object.keys(en), tK = Object.keys(th);
  const eSet = new Set(eK), tSet = new Set(tK);
  return { inEnOnly: eK.filter(k => !tSet.has(k)), inThOnly: tK.filter(k => !eSet.has(k)) };
}
